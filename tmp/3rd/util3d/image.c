/* Select an OpenGL internal texture format for an image with c channels and  */
/* b bytes per channel.                                                       */

int image_internal_form(int c, int b)
{
    assert((1 <= c) && (c <= 4));
    assert((1 <= b) && (b <= 4));

    if (b == 1)
        switch (c)
        {
        case  1: return GL_LUMINANCE;
        case  2: return GL_LUMINANCE_ALPHA;
        case  3: return GL_RGB;
        default: return GL_RGBA;
        }
    if (b == 2)
        switch (c)
        {
        case  1: return GL_LUMINANCE16;
        case  2: return GL_LUMINANCE16_ALPHA16;
        case  3: return GL_RGB16;
        default: return GL_RGBA16;
        }
    if (b == 4)
        switch (c)
        {
        case  1: return GL_RGB32F;
        case  2: return GL_RGB32F;
        case  3: return GL_RGB32F;
        default: return GL_RGBA32F;
        }

    return 0;
}

/* Select an OpenGL external texture format for an image with c channels.     */

int image_external_form(int c)
{
    assert((1 <= c) && (c <= 4));

    switch (c)
    {
    case  1: return GL_LUMINANCE;
    case  2: return GL_LUMINANCE_ALPHA;
    case  3: return GL_RGB;
    default: return GL_RGBA;
    }
}

/* Select an OpenGL data type for an image with b bytes per channel.          */

int image_external_type(int b)
{
    assert((1 <= b) && (b <= 4));

    if (b == 1) return GL_UNSIGNED_BYTE;
    if (b == 2) return GL_UNSIGNED_SHORT;
    if (b == 4) return GL_FLOAT;

    return 0;
}

